# Variables
num = 5
name = "Appu"
# print("value of num is :"+num)
print("value of name is : "+name)   
print("num values is : "+str(num))

# Camel Case
myVariableName = "John"
print(myVariableName)

# Pascal Case
MyVariableName = "John"
print(MyVariableName)

# Snake Case    
my_variable_name = "John"
print(my_variable_name)

z = 4
z = "OK"
print(z)

# Casting
x1 = str(3)
y1 = int(3)
z1 = float(3)
print(x1, y1, z1)

# Get the Type
print(type(x1), type(y1), type(z1))

# Single or Double Quotes
a = 'abcd'
ab = "abcd"
print(type(a), type(ab))

# Case-Sensitive - Varibale Names are Case-Sensitive
xyz = "4"
XYZ = "Appu"
XyZ = "Bijapur"
# XyZ won't overwrite XYZ/xyz values
print("xyz : "+xyz)
print("XYZ : "+XYZ)
print("XyZ : "+XyZ)